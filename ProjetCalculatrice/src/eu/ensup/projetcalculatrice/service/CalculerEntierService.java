package eu.ensup.projetcalculatrice.service;

public class CalculerEntierService {
	
	public int addition(int a, int b) {
		int resultat = a + b;
		return resultat;
	}
	
	public int soustraction(int a, int b) {
		int resultat = a - b;
		return resultat;
	}
	
	public int multiplication(int a, int b) {
		int resultat = a * b;
		return resultat;
	}
	
	public int division(int a, int b) {
		int resultat = a / b;
		return resultat;
	}
}
